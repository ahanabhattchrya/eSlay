class Item {
    constructor(
        itemId,
        name,
        price,
        description,
        status,
        currBid,
        maxBid,
        minBid
    ) {

        this.itemId = itemId;
        this.name = name;
        this.price = price;
        this.description = description;
        this.status = status;
        this.currBid = currBid;
        this.maxBid = maxBid;
        this.minBid = minBid;
    }
};